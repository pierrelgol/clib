#include "../../include/clib.h"

int32_t	string_casecompare(const uint8_t *s1, const uint8_t *s2)
{
	while (*s1 && (*s1 | 32) == (*s2 | 32))
	{
		++s1;
		++s2;
	}
	return ((int32_t)(*s1 | 32) - (*s2 | 32));
}
